/*
   Printing Methods in Java
   Concepts:
   - println()
   - print()
   - printf()
*/

public class PrintTypes {

    public static void main(String[] args) {

        // Moves cursor to next line
        System.out.println("Java");

        // Prints in same line
        System.out.print("DSA ");

        // Formatted printing
        int age = 21;
        System.out.printf("Age: %d", age);
    }
}