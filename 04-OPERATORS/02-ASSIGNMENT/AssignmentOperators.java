/*
   Assignment Operators
   Concepts:
   =   Assign
   +=  Add and assign
   -=  Subtract and assign
   *=  Multiply and assign
   /=  Divide and assign
*/

public class AssignmentOperators {

    public static void main(String[] args) {

        int number = 10;

        number += 5;
        System.out.println(number);

        number -= 3;
        System.out.println(number);

        number *= 2;
        System.out.println(number);

        number /= 4;
        System.out.println(number);
    }
}