/*
   Relational Operators
   Concepts:
   ==  Equal to
   !=  Not equal to
   >   Greater than
   <   Less than
   >=  Greater than or equal to
   <=  Less than or equal to
*/

public class RelationalOperators {

    public static void main(String[] args) {

        int a = 15;
        int b = 10;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}