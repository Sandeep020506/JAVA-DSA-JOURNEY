/*
   Non-Primitive Data Types
   Concepts:
   - String
   - Array
*/

public class NonPrimitiveDataTypes {

    public static void main(String[] args) {

        // String
        String name = "Sandeep";

        // Array
        int[] numbers = {10, 20, 30};

        System.out.println(name);
        System.out.println(numbers[0]);
    }
}